# sep as no space
print("Hello", "World", 123, True, 3.14, sep="")
# sep as single space
print("Hello", "World", 123, True, 3.14, sep=" ")
# sep as double space
print("Hello", "World", 123, True, 3.14, sep="  ")
# sep as @
print("Hello", "World", 123, True, 3.14, sep="@")
# sep as #
print("Hello", "World", 123, True, 3.14, sep="#")
# sep as -
print("Hello", "World", 123, True, 3.14, sep="-")
# sep as _
print("Hello", "World", 123, True, 3.14, sep="_")
#single argument
print("Revathi")
#two arguments
print("Revathi","Poorna")
#three arguments
print("Revathi","Poorna","Sharnika")
#four arguments
print("Revathi","Poorna","Sharnika","Aksharan")